<div class="Work">
    <p class="Text1">Sebelum ia menjadi guru di sekolah kita, ia pernah bekerja sebagai seorang sopir antar-jemput yang mengantarkan tamu dari bandara ke hotel. Setelah itu, Pak Arie akhirnya menjadi guru di SMK TI Bali Global.</p>
    <p class="Text1"><em>Tetapi... kenapakah ia menjadi guru di SMK TI?</em> Tetapi... kenapakah dia menjadi guru di SMK TI? Jadi pada awalnya, Bapak memang memiliki <em>passion</em> di dunia teknologi. Ia suka koding dan aktivitas-aktivitas lainnya yang berkaitan dengan teknologi. Ia pun menjadi seorang "teknisi dadakan" untuk teman-teman dengan laptop yang tiba-tiba rusak. Selama melakukan ini, Bapak sadar bahwa berbagi ilmu tentang teknologi ternyata cukup seru untuknya.</p>
    <p class="Text1">Jadi, Pak Arie memutuskan untuk mencoba menjadi guru. Selama disini, ia juga ingin mengasah skill <em>public speaking</em>. Jika tidak cocok, ia bisa saja mencari pekerjaan lain. Pada awal dia mengajar, ia berpikir bahwa menjadi guru akan hanya menjadi sebuah <em>"batu loncatan"</em>. Sambil mengajar, bisa saja ada peluang baru yang terbuka yang bisa Bapak ambil. Tetapi, lama-kelamaan, ia semakin betah. Dia suka melihat siswa-siswa yang tumbuh dengan projek-projek mereka, dia suka melihat orang-orang yang baru dan bertemu kembali dengan orang yang lama tidak ketemuan. Pak Arie merasa bahwa mengajar benar-benar pas untuknya.</p>
    <p class="Text1">Tetapi kenapa dia memilih SMK TI? Sebenarnya ia hanya ingin bertemu dengan guru-guru lama. Bisa saja ada rasa kangen atau pun nostalgia untuk sekolah ini. Ia dapat bekerja dengan guru-guru yang dulu ditakuti dan juga yang disuka. Memang aneh bahwa awalnya dia dapat takut kepada guru A, tetapi sekarang malahan dapat ngobrol santai sama mereka.</p>
</div>

